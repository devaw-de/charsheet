export function WizardCantripsKnownPerLevel(level: number): number {
  if (level < 1 || level > 20) {
    return 0;
  }

  switch (true) {
    case level < 4:
      return 3;
    case level < 9:
      return 4;
    default:
      return 5;
  }
}
